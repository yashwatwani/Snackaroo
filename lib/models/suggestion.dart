class Suggestion {
  final String title;
  final String emoji;
  final String tip;
  final String recipe;
  final String imageUrl;
  final int calories;
  final int carbs;
  final int protein;
  final int fat;

  Suggestion({
    required this.title,
    required this.emoji,
    required this.tip,
    required this.recipe,
    required this.imageUrl,
    required this.calories,
    required this.carbs,
    required this.protein,
    required this.fat,
  });
}
